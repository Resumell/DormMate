void oled_clear(void);
void oled_init(void) {
    i2c_master_bus_config_t bus_cfg = {
        .i2c_port = I2C_NUM_0,
        .sda_io_num = OLED_SDA,
        .scl_io_num = OLED_SCL,
        .clk_source = I2C_CLK_SRC_DEFAULT,
        .glitch_ignore_cnt = 7,
        .flags.enable_internal_pullup = true,
    };
    i2c_master_bus_handle_t bus;
    ESP_ERROR_CHECK(i2c_new_master_bus(&bus_cfg, &bus));

    /* 探测 OLED 是否在线 */
    esp_err_t ret = i2c_master_probe(bus, OLED_ADDR, -1);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "OLED 地址 0x%02X 无应答！检查接线或改成 0x3D", OLED_ADDR);
    } else {
        ESP_LOGI(TAG, "OLED I2C 探测成功，地址 0x%02X", OLED_ADDR);
    }

    i2c_device_config_t dev_cfg = {
        .dev_addr_length = I2C_ADDR_BIT_LEN_7,
        .device_address = OLED_ADDR,
        .scl_speed_hz = 100000,  // 降到 100kHz，杜邦线更稳
    };
    ESP_ERROR_CHECK(i2c_master_bus_add_device(bus, &dev_cfg, &oled_dev));

    /* 标准 SSD1306 初始化序列 */
    oled_cmd(0xAE); oled_cmd(0xD5); oled_cmd(0x80);
    oled_cmd(0xA8); oled_cmd(0x3F); oled_cmd(0xD3); oled_cmd(0x00);
    oled_cmd(0x40); oled_cmd(0x8D); oled_cmd(0x14);
    oled_cmd(0x20); oled_cmd(0x00); // 水平寻址
    oled_cmd(0xA1); oled_cmd(0xC8);
    oled_cmd(0xDA); oled_cmd(0x12);
    oled_cmd(0x81); oled_cmd(0xCF);
    oled_cmd(0xD9); oled_cmd(0xF1);
    oled_cmd(0xDB); oled_cmd(0x40);
    oled_cmd(0xA4); oled_cmd(0xA6);
    oled_cmd(0xAF); // 开显示

    oled_clear();
    ESP_LOGI(TAG, "OLED 初始化完成");
}

/* 清屏：逐页写 0 */
void oled_clear(void) {
    uint8_t empty[128] = {0};
    for (int page = 0; page < 8; page++) {
        oled_cmd(0xB0 + page);
        oled_cmd(0x00);
        oled_cmd(0x10);
        oled_data_page(empty, 128);
    }
}

void oled_show(const char* line1, const char* line2) {
    uint8_t page0[128] = {0};
    uint8_t page1[128] = {0};

    /* 渲染第一行到 page0 */
    int x = 0;
    for (const char* p = line1; *p && x < 21; p++) {
        if (*p < 32 || *p > 126) continue;
        uint8_t *glyph = (uint8_t*)font6x8[*p - 32];
        for (int col = 0; col < 6 && (x * 6 + col) < 128; col++) {
            page0[x * 6 + col] = glyph[col];
        }
        x++;
    }

    /* 渲染第二行到 page1 */
    x = 0;
    for (const char* p = line2; *p && x < 21; p++) {
        if (*p < 32 || *p > 126) continue;
        uint8_t *glyph = (uint8_t*)font6x8[*p - 32];
        for (int col = 0; col < 6 && (x * 6 + col) < 128; col++) {
            page1[x * 6 + col] = glyph[col];
        }
        x++;
    }

    /* 按页刷新 */
    oled_cmd(0xB0); oled_cmd(0x00); oled_cmd(0x10);
    oled_data_page(page0, 128);

    oled_cmd(0xB1); oled_cmd(0x00); oled_cmd(0x10);
    oled_data_page(page1, 128);

    ESP_LOGI(TAG, "OLED显示: [%s] / [%s]", line1 ? line1 : "", line2 ? line2 : "");
}