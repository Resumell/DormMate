#define OLED_SDA  8
#define OLED_SCL  9
#define OLED_ADDR 0x3C   // 如果还是雪花，改成 0x3D

static i2c_master_dev_handle_t oled_dev = NULL;
static const char* TAG = "OLED";

/* 发送单条命令 */
static void oled_cmd(uint8_t c) {
    uint8_t buf[2] = {0x00, c};
    if (oled_dev) i2c_master_transmit(oled_dev, buf, 2, -1);
}

/* 发送一页数据（最多 128 字节） */
static void oled_data_page(const uint8_t *data, int len) {
    uint8_t buf[129];
    if (len > 128) len = 128;
    buf[0] = 0x40;
    memcpy(buf + 1, data, len);
    if (oled_dev) i2c_master_transmit(oled_dev, buf, len + 1, -1);
}

