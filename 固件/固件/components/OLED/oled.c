static i2c_master_dev_handle_t i2c_dev = NULL;
static i2c_master_bus_handle_t i2c_bus = NULL;
static uint8_t oled_buffer[SSD1306_PAGES][SSD1306_WIDTH];

static esp_err_t oled_write_cmd(uint8_t cmd)
{
    uint8_t data[2] = {I2C_CMD_BYTE, cmd};
    return i2c_master_transmit(i2c_dev, data, 2, -1);
}

static esp_err_t oled_write_data(const uint8_t *data, size_t len)
{
    // Prepend data byte marker
    uint8_t *buf = malloc(len + 1);
    if (!buf) return ESP_ERR_NO_MEM;
    buf[0] = I2C_DATA_BYTE;
    memcpy(buf + 1, data, len);
    esp_err_t ret = i2c_master_transmit(i2c_dev, buf, len + 1, -1);
    free(buf);
    return ret;
}

esp_err_t oled_init(int sda_pin, int scl_pin, uint8_t i2c_addr)
{
    // Init I2C bus
    i2c_master_bus_config_t bus_cfg = {
        .i2c_port = I2C_NUM_0,
        .sda_io_num = sda_pin,
        .scl_io_num = scl_pin,
        .clk_source = I2C_CLK_SRC_DEFAULT,
        .glitch_ignore_cnt = 7,
        .flags.enable_internal_pullup = true,
    };
    esp_err_t ret = i2c_new_master_bus(&bus_cfg, &i2c_bus);
    if (ret != ESP_OK) return ret;

    // Add device
    i2c_device_config_t dev_cfg = {
        .dev_addr_length = I2C_ADDR_BIT_LEN_7,
        .device_address = i2c_addr,
        .scl_speed_hz = I2C_MASTER_FREQ_HZ,
    };
    ret = i2c_master_bus_add_device(i2c_bus, &dev_cfg, &i2c_dev);
    if (ret != ESP_OK) return ret;

    // SSD1306 init sequence
    oled_write_cmd(0xAE); // Display off
    oled_write_cmd(0xD5); // Set display clock divide
    oled_write_cmd(0x80);
    oled_write_cmd(0xA8); // Set multiplex
    oled_write_cmd(0x3F); // 64
    oled_write_cmd(0xD3); // Set display offset
    oled_write_cmd(0x00);
    oled_write_cmd(0x40); // Set start line
    oled_write_cmd(0x8D); // Charge pump
    oled_write_cmd(0x14); // Enable
    oled_write_cmd(0x20); // Memory mode
    oled_write_cmd(0x00); // Horizontal
    oled_write_cmd(0xA1); // Segment remap
    oled_write_cmd(0xC8); // COM scan direction
    oled_write_cmd(0xDA); // COM pins
    oled_write_cmd(0x12);
    oled_write_cmd(0x81); // Contrast
    oled_write_cmd(0xCF);
    oled_write_cmd(0xD9); // Pre-charge
    oled_write_cmd(0xF1);
    oled_write_cmd(0xDB); // VCOM detect
    oled_write_cmd(0x40);
    oled_write_cmd(0xA4); // Entire display on (resume)
    oled_write_cmd(0xA6); // Normal display
    oled_write_cmd(0xAF); // Display on

    oled_clear();
    oled_refresh();
    return ESP_OK;
}

void oled_clear(void)
{
    memset(oled_buffer, 0, sizeof(oled_buffer));
}

void oled_fill(void)
{
    memset(oled_buffer, 0xFF, sizeof(oled_buffer));
}

