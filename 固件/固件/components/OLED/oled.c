{
    if (line >= SSD1306_PAGES / 2) return; // 0-3 lines for 8x16 font
    uint8_t page = line * 2;

    for (int col = 0; *str && col < SSD1306_WIDTH - 8; str++, col += 8) {
        uint8_t c = (uint8_t)*str;
        if (c < 32 || c > 126) c = 32;
        const uint8_t *glyph = font8x16[c - 32];

        for (int i = 0; i < 8; i++) {
            oled_buffer[page][col + i] = glyph[i];
            oled_buffer[page + 1][col + i] = glyph[i + 8];
        }
    }
}

esp_err_t oled_refresh(void)
{
    for (uint8_t page = 0; page < SSD1306_PAGES; page++) {
        oled_write_cmd(0xB0 + page);  // Set page
        oled_write_cmd(0x00);         // Set low column
        oled_write_cmd(0x10);         // Set high column
        oled_write_data(oled_buffer[page], SSD1306_WIDTH);
    }
    return ESP_OK;
}
